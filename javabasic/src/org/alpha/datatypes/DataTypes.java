//Data types in java
package org.alpha.datatypes;

public class DataTypes {
    public static void main(String[] args){
        //Data Types in Java
        //Integer
        byte a = 12; //range -128 to 127
        short b = 2000; //range -32,768 to 32,767
        int c = 300000000; //range -2,147,483,648 to 2,147,483,647
        long d = 1000000000000000000L; //range -9,223,372,036,854,775,808 to -9,223,372,036,854,775,807

        //character
        char e = '4'; //use single quote
        char f = '$';

        //Float
        float g = 34.33333F; //range upto 5 decimal point value
        double h = 34.333333333; //range upto 15 decimal point value

        //Boolean - true and false
        boolean i = true;
        boolean j = false;

        //comments in java

        //1. This is single line comment

        /*
        2. This is
        multiline
        comment.
         */



    }
}
